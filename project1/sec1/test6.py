# 인덱스 및 슬라이싱
name = "kim gi tae is 1004"
print("name[0] : ", name[0])    # 인덱스가 0인 데이터
print("name[2:6] : ", name[2:6])    # 인덱스가 2부터 6까지의 문자열
print("len(name) : ", len(name))    # len(변수) : 문자열의 길이(공백 포함)
print("name[2:]", name[2:])     # 인덱스가 2 부터 끝까지
print("name[:2]", name[:2])     # 처음부터 x 까지
print("name[::2]", name[::2])   # 변수[::2] 인덱스가 짝수 번째인 값
print("name[-2:]", name[-2:])   # 값이 음수이면, 오른쪽 부터